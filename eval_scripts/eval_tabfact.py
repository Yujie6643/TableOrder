import argparse
import json
import re


def normalize_label(text):
    text = str(text or "").strip().lower()
    text = text.split("</s>", 1)[0].strip()
    text = re.sub(r"[^a-z]+", " ", text).strip()
    if not text:
        return ""

    words = text.split()
    if "entailed" in words:
        return "entailed"
    if "refuted" in words:
        return "refuted"
    return words[0]


def main(args):
    data = []
    with open(args.pred_file, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                data.append(json.loads(line))

    total = 0
    correct = 0
    for item in data:
        gold = normalize_label(item.get("output"))
        pred = normalize_label(item.get("predict"))
        total += 1
        correct += int(gold == pred)

    acc = correct / total if total else 0.0
    print(f"acc={acc:.4f}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Evaluate TabFact entailed/refuted predictions.")
    parser.add_argument(
        "--pred_file",
        type=str,
        default="/data/tyj/2D-TPE-main/res/tabfact_adaptive_start_0p8_res.json",
        help="JSONL prediction file generated by inference.py",
    )
    args = parser.parse_args()
    main(args)
